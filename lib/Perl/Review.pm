=head1 NAME

Perl::Review - Engine to critique Perl souce code

=head1 DESCRIPTION

The 'Perl::Review' module has been discontinued.  To avoid possible
confusion with B<The Perl Review> magazine, I have changed the name to
L<Perl::Critic>.  Please search for L<Perl::Critic> on CPAN to find
current and future releases.  While you're at it, check out
L<http://www.theperlreview.com>.  It's a great little mag!

